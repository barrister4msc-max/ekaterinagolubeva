export function getGenerationProfile(
  templateCode: string | null | undefined,
): GenerationProfile | null {
  if (!templateCode) return null;
  return PROFILES[templateCode] ?? null;
}

export function renderTemplateProfileBlock(
  profile: GenerationProfile,
): string {
  return `
==========================================================
TEMPLATE-SPECIFIC DOCUMENT RULES
==========================================================

Эти правила применяются ДОПОЛНИТЕЛЬНО к универсальным DOCUMENT GENERATION RULES
и DOCUMENT COMPLETENESS RULES. Они НЕ заменяют универсальные правила,
а определяют форму и структуру конкретного типа документа.

GENERATION PROFILE: ${profile.generation_profile}
DOCUMENT TYPE:      ${profile.document_type}
REVIEW PROFILE:     ${profile.review_profile}
TEMPLATE CODE:      ${profile.template_code}

НАЗНАЧЕНИЕ ДОКУМЕНТА:
${profile.purpose}

ОБЯЗАТЕЛЬНАЯ СТРУКТУРА (по возможности, с учётом имеющихся данных):
${profile.required_structure.map((s, i) => `${i + 1}. ${s}`).join("\n")}

СПЕЦИАЛЬНЫЕ ПРАВИЛА:
${profile.special_rules.map((s) => `- ${s}`).join("\n")}

ЗАПРЕЩЕНО:
${profile.forbidden_patterns.map((s) => `- ${s}`).join("\n")}

ПРАВИЛО ОТСУТСТВУЮЩИХ ДАННЫХ:
${profile.missing_data_rule}

В поле "document_type" итогового JSON верни:
"${profile.document_type}"
==========================================================
`;
}
